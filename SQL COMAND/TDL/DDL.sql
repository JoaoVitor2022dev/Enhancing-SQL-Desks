CREATE – Criar objetos no banco de dados (tabelas, índices, esquemas, etc.).
ALTER – Modificar a estrutura de objetos existentes (adicionar ou remover colunas, alterar tipos de dados, etc.).
DROP – Remover objetos do banco de dados (excluir tabelas, índices, etc.).
TRUNCATE – Remover todos os registros de uma tabela sem registrar no log de transações.
COMMENT – Adicionar comentários à estrutura do banco de dados.
RENAME – Renomear objetos do banco de dados.